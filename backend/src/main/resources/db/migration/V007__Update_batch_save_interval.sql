-- V007: 배치 저장 간격 최적화
-- 날짜: 2026-01-27
-- 설명: 배치 저장 간격을 100ms에서 5000ms로 변경하여 성능 최적화
-- =============================================================================

-- 배치 저장 간격: 100ms → 5000ms (5초)
-- 이유: 100ms 타이머와 동일한 간격이면 매번 1건씩 저장되어 배치 효과 없음
UPDATE settings
SET value = '5000'
WHERE key = 'system.storage.saveInterval';

